package com.web.hulklogic.utils;

public class Invite {
private String inviteAgent;
private String  referralcode;
private String firstname;
private String sellerName;
private String sellerAddress;
private String shopname;
private Long sellerMobileNumber;
public String getSellerName() {
	return sellerName;
}
public void setSellerName(String sellerName) {
	this.sellerName = sellerName;
}
public String getSellerAddress() {
	return sellerAddress;
}
public void setSellerAddress(String sellerAddress) {
	this.sellerAddress = sellerAddress;
}
public String getShopname() {
	return shopname;
}
public void setShopname(String shopname) {
	this.shopname = shopname;
}
public Long getSellerMobileNumber() {
	return sellerMobileNumber;
}
public void setSellerMobileNumber(Long sellerMobileNumber) {
	this.sellerMobileNumber = sellerMobileNumber;
}
public String getInviteAgent() {
	return inviteAgent;
}
public void setInviteAgent(String inviteAgent) {
	this.inviteAgent = inviteAgent;
}
public String getReferralcode() {
	return referralcode;
}
public void setReferralcode(String referralcode) {
	this.referralcode = referralcode;
}
public String getFirstname() {
	return firstname;
}
public void setFirstname(String firstname) {
	this.firstname = firstname;
}


}
